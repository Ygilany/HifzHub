// Types for Quran data structures

export interface PageLine {
  pageNumber: number;
  lineNumber: number;
  lineType: 'surah_name' | 'basmallah' | 'ayah';
  isCentered: boolean;
  firstWordId: number | null;
  lastWordId: number | null;
  surahNumber: number | null;
}

export interface Verse {
  id: number;
  verseKey: string;
  surah: number;
  ayah: number;
  text: string;
}

export interface Word {
  id: number;
  text: string;
  verseKey: string;
  position: number;
}

export interface LineInfo {
  lineType: 0 | 1 | 2; // 0 = normal ayah, 1 = surah name, 2 = basmallah
  lineWidthRatio: number;
  isCentered: boolean;
}

export interface PageData {
  pageNumber: number;
  lines: string[];
  lineInfos: LineInfo[];
}

